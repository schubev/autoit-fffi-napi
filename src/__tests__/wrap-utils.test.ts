import { inWstrOfString } from '../wrap-utils'

describe(inWstrOfString, () => {
  it.each([
    ['', [0x00, 0x00]],
    ['*', [0x2a, 0x00, 0x00, 0x00]],
    ['**', [0x2a, 0x00, 0x2a, 0x00, 0x00, 0x00]],
    ['test', [0x74, 0x00, 0x65, 0x00, 0x73, 0x00, 0x74, 0x00, 0x00, 0x00]],
    ['téẞt', [0x74, 0x00, 0xe9, 0x00, 0x9e, 0x1e, 0x74, 0x00, 0x00, 0x00]],
  ])('%p encodes to %p', (input, expected) => {
    expect(inWstrOfString(input)).toEqual(Buffer.from(expected))
  })
})
